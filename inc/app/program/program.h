//=============================================================================
/// @file
/// プログラムヘッダファイル
///
/// プログラムヘッダファイルです。
///
/// @attention アプリケーションを起動するための共通クラスです。
///            このファイルをインクルードした場合は、必ずプログラムクラスのメイン関数(Program::Main)を実装してください。
//-----------------------------------------------------------------------------

#pragma once
#pragma managed(push, off)

//=============================================================================
// インクルードファイル
//-----------------------------------------------------------------------------
#include "program/program_def.h"

//=============================================================================
// プログラム名前空間
//-----------------------------------------------------------------------------
namespace program {
    //=========================================================================
    // グローバル関数
    //-------------------------------------------------------------------------
    /// 名前空間名取得関数
    ///
    /// 名前空間名取得関数です。
    ///
    /// @param     なし
    /// @return    文字列ポインタ
    /// @attention なし
    //-------------------------------------------------------------------------
    wchar_t const* GetNameSpaceName() noexcept;

    //-------------------------------------------------------------------------
    /// ターゲット種別取得関数
    ///
    /// ターゲット種別取得関数です。
    ///
    /// @param     なし
    /// @return    文字列ポインタ
    /// @attention なし
    //-------------------------------------------------------------------------
    wchar_t const* GetTargetType() noexcept;

    //=========================================================================
    /// プログラムクラス
    ///
    /// プログラムクラスです。
    ///
    /// @attention 非インスタンス、シングルトンクラスです。
    //-------------------------------------------------------------------------
    class Program final {
        //=====================================================================
        // 非公開削除関数
        //---------------------------------------------------------------------
    private:
        //---------------------------------------------------------------------
        /// コピーコンストラクタ
        ///
        /// コピーコンストラクタです。
        ///
        /// @param[in] インスタンス参照
        /// @return    なし
        /// @attention この関数を削除しました。
        //---------------------------------------------------------------------
        Program(Program const&) = delete;

        //---------------------------------------------------------------------
        /// インスタンスコピー代入演算子オーバーロード関数
        ///
        /// インスタンスコピー代入演算子オーバーロード関数です。
        ///
        /// @param[in] インスタンス参照
        /// @return    なし
        /// @attention この関数を削除しました。
        //---------------------------------------------------------------------
        Program& operator=(Program const&) = delete;

        //=====================================================================
        // インライン静的非公開文字列定数
        //---------------------------------------------------------------------
    private:
        inline static constexpr wchar_t const* const class_name{L"プログラムクラス"}; ///< クラス名

        //=====================================================================
        // 静的公開関数
        //---------------------------------------------------------------------
    public:
        //---------------------------------------------------------------------
        /// 開始関数
        ///
        /// 開始関数です。
        ///
        /// @param[in] hInstance インスタンスハンドル
        /// @return    実行結果
        /// -          true  成功
        /// -          false 失敗
        /// @attention なし
        //---------------------------------------------------------------------
        static bool Startup(HINSTANCE hInstance) noexcept;

        //---------------------------------------------------------------------
        /// メイン関数
        ///
        /// メイン関数です。
        ///
        /// @param[in] hInstance インスタンスハンドル
        /// @return    実行結果
        /// -          true  成功
        /// -          false 失敗
        /// @attention アプリケーション側で実装が必要です。
        //---------------------------------------------------------------------
        static bool Main(HINSTANCE hInstance) noexcept;

        //=====================================================================
        // 非公開構築子と解体子
        //---------------------------------------------------------------------
    private:
        //---------------------------------------------------------------------
        /// コンストラクタ
        ///
        /// コンストラクタです。
        ///
        /// @param     なし
        /// @return    なし
        /// @attention この関数を削除しました。
        //---------------------------------------------------------------------
        Program() = delete;

        //---------------------------------------------------------------------
        /// デストラクタ
        ///
        /// デストラクタです。
        ///
        /// @param     なし
        /// @return    なし
        /// @attention この関数を削除しました。
        //---------------------------------------------------------------------
        ~Program() = delete;

        //=====================================================================
        // インライン静的公開関数
        //---------------------------------------------------------------------
    public:
        //---------------------------------------------------------------------
        /// オブジェクト名取得関数
        ///
        /// オブジェクト名取得関数です。
        ///
        /// @param     なし
        /// @return    文字列ポインタ
        /// @attention なし
        //---------------------------------------------------------------------
        inline static wchar_t const* GetObjectName() noexcept { return Program::class_name; }
    };
}

#pragma managed(pop)
